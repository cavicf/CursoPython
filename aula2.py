#o comando sep='' pode mudar o separador padrão dos argumentos não nomeados
#o comando end='' pode mudar o fim padrão dos argumentos não nomeados
#\r \n são os line feed do windows que fazem a quebra de linha -> CRLF
print(12, 34, 1011, sep='-', end='#')
print(56, 78, sep="-")
print(9, 10, sep="-")
